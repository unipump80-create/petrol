"""Нормализация названий брендов АЗС.

OSM и russiabase пишут бренды по-разному (с «ООО», кавычками, латиницей).
Приводим к единому ключу, чтобы не плодить дубли и связывать цены с точками.
"""
import re

# Любой вариант написания -> канонический бренд (сравнение по подстроке)
BRAND_ALIASES = {
    "лукойл": "Лукойл",
    "lukoil": "Лукойл",
    "газпромнефть": "Газпромнефть",
    "газпром нефть": "Газпромнефть",
    "gazpromneft": "Газпромнефть",
    "газойл": "Газойл",
    "опти": "Опти",
    "opti": "Опти",
    "татнефть": "Татнефть",
    "tatneft": "Татнефть",
    "роснефть": "Роснефть",
    "rosneft": "Роснефть",
    "ока-пропан": "ОКА-ПРОПАН",
    "ока пропан": "ОКА-ПРОПАН",
    "нефтепарк": "НефтеПарк",
    "нефть-транзит": "Нефть-Транзит",
    "альянс ойл": "Альянс Ойл",
    "бизнес комтраст": "Бизнес Комтраст",
    "ивнефтепродукт": "Ивнефтепродукт",
    "смарт-технологии": "Смарт-Технологии",
    "фьюел технолод": "Fuel",
    "промресурс": "Промресурс",
    "альянс ойл": "Альянс Ойл",
    "mannol": "Mannol",
    "итк": "ИТК",
    "агнкс": "АГНКС",
}

# Юр. формы и мусор, который срезаем перед сравнением
_PREFIX_RE = re.compile(
    r'^(ооо|оао|зао|пао|ао|ип|тд|тк|нпф|сеть\s+азс|азс)\b[\s.""«»-]*',
    re.IGNORECASE,
)
_QUOTES = '"«»\'`'


def normalize_brand(raw: str | None) -> str | None:
    """Сырое название бренда -> канонический вид (или None)."""
    if not raw:
        return None
    key = raw.strip().strip(_QUOTES).lower()
    # срезаем юр. форму (возможно несколько раз: ООО ТД "X")
    prev = None
    while key != prev:
        prev = key
        key = _PREFIX_RE.sub("", key).strip().strip(_QUOTES)

    if key in BRAND_ALIASES:
        return BRAND_ALIASES[key]
    for alias, canonical in BRAND_ALIASES.items():
        if alias in key:
            return canonical
    # ничего не нашли — вернём очищенное название с заглавной
    cleaned = raw.strip().strip(_QUOTES)
    cleaned = _PREFIX_RE.sub("", cleaned).strip().strip(_QUOTES)
    return cleaned or raw.strip()
